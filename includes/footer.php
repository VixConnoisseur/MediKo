    <script>
        function updateTime() {
            const timeElement = document.getElementById('current-time');
            if (timeElement) {
                const now = new Date();
                const options = {
                    weekday: 'long',
                    year: 'numeric',
                    month: 'long',
                    day: 'numeric',
                    hour: 'numeric',
                    minute: 'numeric',
                    second: 'numeric',
                    hour12: true
                };
                timeElement.textContent = now.toLocaleString('en-US', options);
            }
        }
        setInterval(updateTime, 1000);
        updateTime(); // Initial call
    </script>
